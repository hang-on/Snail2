.section "Misc. functions" free
; ====================================================================
; Output
; --------------------------------------------------------------------
LoadCommandWord:
             push  af
             ld    a, l
             out   (VDPCOM), a
             ld    a, h
             out   (VDPCOM), a
             pop   af
             ret
; ====================================================================

; ====================================================================
; Write BC amount of bytes from data source pointed to be HL
; --------------------------------------------------------------------
WriteToVRAM:
             ld    a, (hl)
             out   (VDPDATA), a
             inc   hl
             dec   bc
             ld    a, c
             or    b
             jp    nz, WriteToVRAM
             ret

.ends


